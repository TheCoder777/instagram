""" Formatted Strings in Python """

name = "Bob"

# with '.format()': #

print("Hello, my name is {}!".format(name))

# with f-strings: #

print(f"Hello, my name is {name}!")

"""
Output:
Hello, my name is Bob!
Hello, my name is Bob!
"""

# In my opinion, F-strings are much more
# practical, especially if you have a lot
# of code, it makes it much more readable.
